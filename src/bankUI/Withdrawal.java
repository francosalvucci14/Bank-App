    private static void withdraw(){
        Connection con = null;
        jTextArea1.setForeground(null);
        jTextArea1.setText("");
        try{
            String searchConto = "select * from utenti inner join credito on utenti.UID = credito.UserID where NConto=?";
            String updateCredito = "update credito inner join utenti on utenti.UID = credito.UserID set Credito = ? where utenti.NConto=?";
            con=DriverManager.getConnection("jdbc:mysql://localhost:3306/BankApp","root","");
            int conto = Integer.parseInt(jTextField1.getText());
            PreparedStatement stmnt = con.prepareStatement(searchConto);
            stmnt.setInt(1, conto);
            ResultSet rs = stmnt.executeQuery();
            
            if(jRadioButton1.isSelected()){
               
                if(!jTextField2.getText().equals("")){
                    buttonGroup1.clearSelection();
                    prelievo = jTextField2.getText();
                }else{
                      prelievo = jRadioButton1.getText(); 
                      jTextField2.setText("");
                }
               
            }
            if(jRadioButton2.isSelected()){
                
               if(!jTextField2.getText().equals("")){
                    buttonGroup1.clearSelection();
                    prelievo = jTextField2.getText();
                }else{
                      prelievo = jRadioButton2.getText(); 
                      jTextField2.setText("");
                }
            }
            if(jRadioButton3.isSelected()){
                
                if(!jTextField2.getText().equals("")){
                    buttonGroup1.clearSelection();
                    prelievo = jTextField2.getText();
                }else{
                      prelievo = jRadioButton3.getText(); 
                      jTextField2.setText("");
                }
            }
            if(!jRadioButton1.isSelected() && !jRadioButton2.isSelected() && !jRadioButton3.isSelected()){
                prelievo = jTextField2.getText();
            }
            
            
            boolean val = rs.next();
            if(val){
                int creditEsist = rs.getInt("Credito");
                int prel = Integer.parseInt(prelievo);
                    if(prel > creditEsist){
                        jTextArea1.setForeground(Color.RED);
                        jTextArea1.append("Error : You cannot withdraw this amount\n");
                        log.warning("--- Withdrawal -> Error : You cannot withdraw this amount");
                        
                    }else{
                        int tot = creditEsist - prel;
                        PreparedStatement updateC = con.prepareStatement(updateCredito);
                        updateC.setInt(1,tot);
                        updateC.setInt(2, conto);
                        updateC.executeUpdate();
                        BankUI.infoBox("Withdrawal successful", "Withdrawal");
                        log.info("--- Withdrawal -> Withdrawal successful");
                    }
            }
            //int contoEsis = rs.getInt("Credito");
            
        }catch(SQLException e){
            jTextArea1.append("---Withdrawal -> Exception : " + e.getMessage());
            log.warning("---Withdrawal -> Exception : " + e.getLocalizedMessage());
        }
    }

