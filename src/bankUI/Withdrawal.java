    private static void op4(){
        Connection con = null;
        try{
            con=DriverManager.getConnection("jdbc:mysql://localhost:3306/BankApp","root","");
            String search = "select * from utenti where NConto=?";
            String accountN = jTextField1.getText();
            int accountNumber = Integer.parseInt(accountN);
            PreparedStatement stmnt = con.prepareStatement(search);
            stmnt.setInt(1, accountNumber);
             ResultSet rs = stmnt.executeQuery();
            boolean val = rs.next();
            if(val){
                jPanel3.setVisible(true);
                
            }
        }catch(SQLException e){
            jTextArea1.append("---Withdrawal -> Exception : " + e.getMessage());
            log.warning("---Withdrawal -> Exception : " + e.getLocalizedMessage());
        }finally {
          try{
                if (con != null){
                    con.close();   
                }
            }catch(SQLException e){
             jTextArea1.append(e.getMessage());
             log.warning("---Withdrawal -> Exception : " + e.getLocalizedMessage() + "\n");
            }
        }
    }
